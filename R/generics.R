#' Cherry class constructor
#'
#' @param x An integer value that represents total number of spins.
#' @param tree Size of the original tree.
#' @param game_diff Progression of the game based on the sequential differences
#'   in the amount of cherries on the tree.
#'
#' @return An object of class cherry
#' @export
as_cherry <- function(x, tree, game_diff){
  structure(x,
            tree = tree,
            game_diff = game_diff,
            class = "cherry")
}
